# アプリのタイトル
st.title("調教タイム ランク評価ツール")
st.caption("全15コースの調教タイム基準データを元にランク判定を行います")

# サイドバーの設定
st.sidebar.header("調教条件を設定")
center = st.sidebar.selectbox("トレセン", list(TRAINING_DATA.keys()))
course = st.sidebar.selectbox("コース", list(TRAINING_DATA[center].keys()))
condition = st.sidebar.selectbox("脚色", ["馬也", "強め・一杯"])

# 基準データの取得
ref_data = TRAINING_DATA[center][course][condition]
available_furlongs = ["6F", "5F", "4F", "3F", "2F", "1F"]
target_furlongs = [f for f in available_furlongs if f in ref_data["古馬平均"]]

st.header(f"タイム入力 ({center} / {course} / {condition})")
st.write("計測したハロンのタイムを入力してください（空欄のハロンはスキップされます）")

# 入力欄の配置
user_times = {}
cols = st.columns(len(target_furlongs))

for i, f in enumerate(target_furlongs):
    with cols[i]:
        val = st.text_input(f"{f} (秒)", key=f)
        if val:
            try:
                user_times[f] = float(val)
            except ValueError:
                st.error("数値を入力してください")

# 判定ボタンの処理
if st.button("ランクを判定する", type="primary"):
    if not user_times:
        st.warning("タイムが入力されていません")
    else:
        st.header("評価結果")
        
        for f, t in user_times.items():
            t_top5 = ref_data["上位5%"].get(f)
            t_top20 = ref_data["上位20%"].get(f)
            t_op_avg = ref_data["古馬OP平均"].get(f)
            t_avg = ref_data["古馬平均"].get(f)
            
            # ランク判定
            if t_top5 and t <= t_top5:
                rank_code = "S"
                detail = f"上位5%以内の超抜時計 (基準: {t_top5}秒以下)"
            elif (t_top20 and t <= t_top20) or (t_op_avg and t <= t_op_avg):
                rank_code = "A"
                detail = f"好時計・オープン級 (上位20%: {t_top20}秒 / OP平均: {t_op_avg}秒)"
            elif t_avg and t <= t_avg:
                rank_code = "B"
                detail = f"古馬一般平均クラス (平均: {t_avg}秒)"
            else:
                rank_code = "C"
                detail = f"平均より遅め (平均: {t_avg}秒)"
            
            # 結果の表示（ランクに応じて枠の色を変化させる）
            title_text = f"{f} の判定: タイム {t}秒 ➡️ ランク【 {rank_code} 】"
            with st.expander(title_text, expanded=True):
                if rank_code == "S":
                    st.success(detail)
                elif rank_code == "A":
                    st.info(detail)
                elif rank_code == "B":
                    st.warning(detail)
                else:
                    st.error(detail)